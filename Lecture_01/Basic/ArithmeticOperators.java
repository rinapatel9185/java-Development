package Dec_9_2024;

public class ArithmeticOperators {
	
	public static void main(String[] args) {
		
		int number1=253;
		int number2=65;
		
		int result;
		
		result=number1+number2;
		
		System.out.println("Result is:"+result);
		result=number1-number2;
		System.out.println("Result is:"+result);
		result=number1*number2;
		System.out.println("Result is:"+result);
		result=number1/number2;
		System.out.println("Result is:"+result);
		result=number1%number2;
		System.out.println("Result is:"+result);
		
	}

}
